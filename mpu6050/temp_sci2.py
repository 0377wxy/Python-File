from sympy import *
# 二元一次方程
x = Symbol('x')
y = Symbol('y')
z = Symbol('z')
k1 = Symbol('k1')
k2 = Symbol('k2')
k3 = Symbol('k3')
solved_value = solve(
    [
        (x + 0.01374 * k1)**2 +
        (y - 0.05656 * k2)**2 +
        (z + 0.90203 * k3)**2 - 0.97936**2,

        (x - 0.96244 * k1)**2 +
        (y - 0.05787 * k2)**2 +
        (z - 0.29565 * k3)**2 - 0.97936**2,

        (x - 0.00871 * k1)**2 +
        (y + 0.98105 * k2)**2 +
        (z - 0.00835 * k3)**2 - 0.97936**2,

        (x + 1.01551 * k1)**2 +
        (y - 0.03314 * k2)**2 +
        (z - 0.07795 * k3)**2 - 0.97936**2,

        (x + 0.01633 * k1)**2 +
        (y - 1.01606 * k2)**2 +
        (z - 0.08625 * k3)**2 - 0.97936**2,

        (x - 0.04388 * k1)**2 +
        (y + 0.10544 * k2)**2 +
        (z - 1.13065 * k3)**2 - 0.97936**2,
    ],
    [x, y, z, k1, k2, k3]
)
print(solved_value)
